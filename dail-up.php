    <td width="531"><h3 align="left">Dial-Up Advantages</h3>
      <p>
	  
One of the benefits of dial-up service is that if you have a land line, all that you need is a dial-up account
 and the modem built into your computer to connect to the Internet. You don't need to have a special line installed
  or have a special DSL modem box. Furthermore, you can take your dial-up account with you and connect to the
   Internet anywhere there's a phone line you can access. This can be useful if you are frequently in areas where you 
   cannot access high speed Internet services.</p>
