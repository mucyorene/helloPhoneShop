      <h1>Dail Up  Connectivity</h1>
      <p> A <strong>dial </strong>- <strong>up connection </strong>is a <strong>connection </strong>from an end user to an Internet service
	   provider (ISP) that can be created over a conventional telephone line without any special arrangement with the telecommunications carrier. <br>
       Dial-up Internet access is a form of Internet access that uses the facilities of the public switched telephone network (PSTN) 
	   to establish a connection .</p>
